// ----------------------truthy vs falsy values---------------------------

// 1. Truthy - Any value which returns true | any value " " ,"kiran"
// 1. Falsy - Any value which returns false | undefined, null, "", NaN, 0

// Examples ->

// "kiran" => truthy
// " " => truthy
// "undefined" => truthy (because of String)
// undefined => falsy
// null => falsy
// "" => falsy
// NaN => falsy
// 0 => falsy
